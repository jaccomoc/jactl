package io.jactl.benchmark;

import io.jactl.Utils;

import java.io.IOException;
import java.io.PrintStream;
import java.io.PrintWriter;
import java.io.UncheckedIOException;
import java.io.Writer;
import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.regex.Matcher;
import java.util.regex.Pattern;
import java.util.stream.Collectors;

public class GenerateClasses {

  static Pattern commentLine         = Pattern.compile("^ *#");
  static Pattern classLine           = Pattern.compile("^class +([A-Z][a-z]*)");
  static Pattern endClassPattern     = Pattern.compile("^ +}");
  static Pattern fieldPattern        = Pattern.compile("([A-Za-z][a-zA-Z<,>.]*) +([a-zA-Z0-9]+);");
  static Pattern attrPattern         = Pattern.compile("([A-Za-z][a-zA-Z<,>.]*) +@([a-zA-Z0-9]+)");
  static Pattern otherPattern        = Pattern.compile("^  ([A-Za-z][a-zA-Z<,>.]*) +([a-zA-Z0-9]+)");
  static Pattern classExtendsPattern = Pattern.compile("class (.*) extends +(\\w*)");
  static Pattern publicPattern       = Pattern.compile("public");
  static Pattern stripInlineComment  = Pattern.compile(" *#.*$");
  static Pattern addPublicPrefix     = Pattern.compile("^( +)");
  static Pattern atSign              = Pattern.compile("@");
  static Pattern classKeyword        = Pattern.compile("\\bclass\\b");

  public static void run(String source, PrintStream out) {
    run(source, new PrintWriter(out));
  }

  public static void run(String source, PrintWriter out) {
    List    types       = new ArrayList<>();
    List<List<String>> fields      = new ArrayList<>();
    List    attributes = new ArrayList<>();
    String  rootClass  = null;
    String  className  = null;
    String  extendsClass = null;
    boolean inClass      = false;
    Map     baseClasses = new HashMap();

    for (String it: source.split("\\n")) {

      // Strip '#' comments
      Matcher matcher = commentLine.matcher(it);
      if (matcher.find()) {
        continue;
      }
      it = stripInlineComment.matcher(it).replaceAll("");

      if (it.startsWith("package")) {
        out.println(it);
        out.println("\n////////////////////////////////////////////////////////////////////\n" +
                    "// File was generated using GenerateClasses.pl in tools directory\n" +
                    "// DO NOT EDIT THIS FILE\n" +
                    "////////////////////////////////////////////////////////////////////\n");
        continue;
      }

      matcher = classLine.matcher(it);
      if (matcher.find()) {
        rootClass = matcher.group(1);
        out.println("public abstract " + it + "\n");
        out.println("  public abstract <T> T accept(Visitor<T> visitor);\n");
        continue;
      }

      if (it.matches("^}")) {
        out.print("\n  public interface Visitor<T> {\n");
        String finalRootClass = rootClass;
        types.forEach(t -> out.print("    T visit" + t + "(" + t + " " + finalRootClass.substring(0, 1).toLowerCase() + finalRootClass.substring(1) + ");\n"));
        out.print("  }\n");
        out.println(it);
        continue;
      }

      matcher = endClassPattern.matcher(it);
      if (matcher.find() && inClass) {
        List<List<String>> constructorFields = new ArrayList();
        List superFields = new ArrayList();
        if (!extendsClass.equals(rootClass)) {
          if (!baseClasses.containsKey(extendsClass)) {
            out.println("Error: Unknown base class " +  extendsClass);
            continue;        // Need to have proper way to generate an error
          }
          List<List<String>> baseFields = (List)baseClasses.get(extendsClass);
          superFields.addAll(baseFields.stream().map(f -> f.get(0)).collect(Collectors.toList()));
          constructorFields.addAll(baseFields);
        }
        constructorFields.addAll(fields);

        // Output constructor
        out.println("    public " + className + "(" + constructorFields.stream().map(f -> f.get(1) + " " + f.get(0)).collect(Collectors.joining(", ")) + ") {");
        if (superFields.size() > 0) { out.print("      super(" + String.join(", ", superFields) + ");\n"); }
        fields.forEach(f -> out.println("      this." + f.get(0) + " = " + f.get(0) + ";"));
        List<String> tokenFields = constructorFields.stream().filter(f -> f.get(1).equals("Token")).map(f -> f.get(0)).collect(Collectors.toList());
        String firstTokenArg = tokenFields.size() > 0 ? tokenFields.get(0) : null;
        // If no super field has a type Token then assign location in subclass
        if (firstTokenArg != null && !superFields.stream().anyMatch(f -> f.equals(firstTokenArg))) {
          out.println("      this.location = " + firstTokenArg + ";");
        }
        out.println("    }");
        out.println("    @Override public <T> T accept(Visitor<T> visitor) { return visitor.visit" + className + "(this); }");

        // Generate toString()
        String fieldValues = constructorFields.stream()
                                              .map(f -> f.get(0))
                                              .map(f -> "\"" + f + "=\" + " + f)
                                              .collect(Collectors.joining(" + \", \" + "));
        out.println("    @Override public String toString() { return \"" + className + "[\" +" + (!fieldValues.equals("") ?" " + fieldValues + " +" : "") + " \"]\"; }");

        // Remember fields in case another class extends us
        baseClasses.put(className, fields);

        // Reset for next class in file
        inClass = false;
        fields = new ArrayList<>();
        attributes = new ArrayList();
        out.println(it);
        continue;
      }

      matcher = fieldPattern.matcher(it);
      if (inClass && matcher.find()) {
        fields.add(Utils.listOf(matcher.group(2), matcher.group(1)));
        it = addPublicPrefix.matcher(it).replaceAll("$1public ");
      }
      matcher = attrPattern.matcher(it);
      if (inClass && matcher.find()) {
        attributes.add(matcher.group(2));
        it = addPublicPrefix.matcher(it).replaceAll("$1public ");
      }
      it = atSign.matcher(it).replaceAll("");

      matcher = classExtendsPattern.matcher(it);
      if (matcher.find()) {
        inClass = true;
        className = matcher.group(1);
        extendsClass = matcher.group(2);
        types.add(className);
        it = classKeyword.matcher(it).replaceAll("public static class");
      }

      matcher = publicPattern.matcher(it);
      if (!inClass && !matcher.find()) {
        matcher = otherPattern.matcher(it);
        if (matcher.find()) {
          it = addPublicPrefix.matcher(it).replaceAll("$1public ");
        }
      }
      out.println(it);
    }
    out.flush();
  }
}

