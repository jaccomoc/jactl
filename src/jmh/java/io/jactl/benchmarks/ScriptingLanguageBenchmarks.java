/**
 * Side-by-side JMH benchmarks comparing Jactl, JEXL, Groovy, MVEL, and SpEL on
 * equivalent scripts.
 * 
 * This benchmark suite was generated by Claude AI including the choice of the
 * individual benchmarks to run.
 * 
 * Run with:
 *   ./gradlew jmh && java -jar build/libs/jactl-*-jmh.jar ScriptingLanguageBenchmarks
 *
 * Each benchmark scenario has _jactl, _jexl, _groovy, _mvel, and _spel variants
 * running the same logic where the engine supports it. Execution benchmarks pre-compile
 * scripts in @Setup so only interpreter/runtime overhead is measured. Compilation
 * benchmarks measure parse + codegen cost.
 *
 * Note on Groovy: the compiled Class&lt;&gt; and Binding are shared (created once at
 * setup), but each benchmark call creates a fresh Script instance via
 * InvokerHelper.createScript(). This mirrors how Groovy must be used in a
 * concurrent embedding: the Class is reusable across threads, but Script
 * instances are not thread-safe and must be per-call (or pooled per-thread).
 * JEXL, Jactl, MVEL, and SpEL compiled expressions are stateless and need no
 * per-call wrapper object.
 *
 * Note on SpEL: Spring Expression Language is a single-expression language. Benchmarks
 * requiring multi-statement scripts or user-defined recursive functions are omitted.
 */
