    test("{true:{false:{null:{if:{z:3},for:3}}}}.true.false.null.if.z", 3);
    test("{true:{false:{null:{if:{z:3},for:3}}}}?.true?.false?.null?.if?.z", 3);
    test("{a:1}['a']", 1);
