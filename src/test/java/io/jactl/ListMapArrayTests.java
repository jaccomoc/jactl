    test("[true:[false:[null:[if:[z:3],for:3]]]].true.false.null.if.z", 3);
    test("[true:[false:[null:[if:[z:3],for:3]]]]?.true?.false?.null?.if?.z", 3);
    test("[a:1]['a']", 1);
    testError("[(1):2]", "map key must be string");
    testError("[a:2,b:3,a:4]", "key 'a' occurs multiple times");
    test("{a:1}.a", 1);
    test("{a:{b:2}}.a.b", 2);
    test("{a:{b:2}}?.a?.b", 2);
    test("{a:{b:2}}?.a?['b']", 2);
