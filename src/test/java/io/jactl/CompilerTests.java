  @Test public void finalVarsAndFields() {
    test("class X { final int i = 3; def f() { i } }; new X().f()", 3);
    test("class X { final def i = [a:1]; def f() { i.a } }; new X().f()", 1);
    testError("class X { def i = [a:1]; def f() { i.a } }; X x = new X(); x.i = null; x.f()", "null value for parent");
    testError("class X { long[] i = [1]; def f() { i[0] } }; X x = new X(); x.i = null; x.f()", "cannot retrieve field from null parent");
    testError("class X { final int i = 3; def f() { i += 3 } }; new X().f()", "cannot modify 'final' field 'i'");
    testError("class X { final int i = 3; def f() { ++i } }; new X().f()", "cannot modify 'final' field 'i'");
    testError("final int i; i = 3", "cannot modify 'final' variable 'i'");
    testError("final int i = 3; int j; j = 2; ++i", "cannot modify 'final' variable 'i'");
    testError("final int i = 3; int j; j = 2; def f = { i = 7 }; f()", "cannot modify 'final' variable 'i'");
    testError("final int i = 3; int j; j = 2; def f = { ++i }; f()", "cannot modify 'final' variable 'i'");
    testError("class X { final int i = 3; def f() { this.i++ } }", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { this.i = 7 } }", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { this.i *= 7 } }", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { this.\"${'i'}\" *= 7 } }; new X().f()", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; X x = new X(); ++x.i", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; X x = new X(); x.i = 7", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; def x = new X(); x.i = 7", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; X x = new X(); x.\"${'i'}\" = 7", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; def x = new X(); x.\"${'i'}\" = 7", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; X x = new X(); x.i++", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; X x = new X(); ++x.i", "cannot modify 'final' field");
    test("class X { final int i = 3; int j = 4; def f() { i } }; X x = new X(); ++x.j", 5);
    testError("class X { final int i = 3; def f() { i } }; X x = new X(); ++x.'i'", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; X x = new X(); ++x['i']", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; X x = new X(); ++x.\"${'i'}\"", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; class Y extends X { def g() { ++i } }; Y y = new Y(); y.g()", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; class Y extends X { def g() { ++this.i } }; Y y = new Y(); y.g()", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; class Y extends X { def g() { ++this['i'] } }; Y y = new Y(); y.g()", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; class Y extends X { def g() { super.i += 7} }; Y y = new Y(); y.g()", "cannot modify 'final' field");
    testError("class X { final int i = 3; def f() { i } }; class Y extends X { def g() { super.\"${'i'}\" += 7} }; Y y = new Y(); y.g()", "dynamic field lookup not supported for super");
  }
  
